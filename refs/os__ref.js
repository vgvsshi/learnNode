const os = require('os');

console.log(os.platform()); //получение платфомы пк

console.log(os.arch()); //получение архитектуры

console.log(os.cpus()); //информация о пк

console.log(os.freemem()); //информация о свободной память

console.log(os.totalmem()); //информация о полном объеме памяти

console.log(os.homedir()); //получение корневой директории

console.log(os.uptime()); //время работы системы в милисекундах

